module ApplicationHelper
  def humanDate(month)
  case month
  when 1
    return "Januar"
  when 2
    return "Februar"
  when 3
    return "Mars"
  when 4
    return "April"
  when 5
    return "Mai"
  when 6
    return "Juni"
  when 7
    return "Juli"
  when 8
    return "August"
  when 9 
    return "September"
  when 10 
    return "Oktober"
  when 11
    return "November"
  when 12
    return "Desember"
    
    
    
  end
    
    
  end
  
end
